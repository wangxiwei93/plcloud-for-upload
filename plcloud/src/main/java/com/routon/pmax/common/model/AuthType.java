package com.routon.pmax.common.model;

public class AuthType {
	
	private String rec_id;
	
	private String company_name;
	
	private String project;
	
	private String auth_client_code;
	
	private String auth_term_code;

	public String getRec_id() {
		return rec_id;
	}

	public void setRec_id(String rec_id) {
		this.rec_id = rec_id;
	}

	public String getCompany_name() {
		return company_name;
	}

	public void setCompany_name(String company_name) {
		this.company_name = company_name;
	}

	public String getProject() {
		return project;
	}

	public void setProject(String project) {
		this.project = project;
	}

	public String getAuth_client_code() {
		return auth_client_code;
	}

	public void setAuth_client_code(String auth_client_code) {
		this.auth_client_code = auth_client_code;
	}

	public String getAuth_term_code() {
		return auth_term_code;
	}

	public void setAuth_term_code(String auth_term_code) {
		this.auth_term_code = auth_term_code;
	}
	
	
}
